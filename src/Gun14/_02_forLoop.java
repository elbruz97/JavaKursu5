package Gun14;

public class _02_forLoop {
    public static void main(String[] args) {
        // 1 den 5 e kadar olan sayilari ekrana yazdiriniz.

        // while loop dongu kac kere donecegi belli degil ise kullanilir

        for (int i=1; i<=5; i++) {  // basi belli, sonu belli, artisi belli ise for loop kullan
            System.out.println("i = " + i);
        }
    }
}
