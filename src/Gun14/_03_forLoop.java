package Gun14;

public class _03_forLoop {
    public static void main(String[] args) {
        // 0 dan 10 (dahil) kadar sayilari yazdiriniz
        // 10 dan 0 (dahil) kadara sayilari yazdiriniz

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }
    }
}
