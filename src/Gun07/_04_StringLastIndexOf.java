package Gun07;

public class _04_StringLastIndexOf {
    public static void main(String[] args) {
        // aramaya sondan baslar

        String s1 = "Merhaba";

        System.out.println("s1.indexOf(a) = " + s1.indexOf("a")); // 4
        System.out.println("s1.lastIndexOf(a) = " + s1.lastIndexOf("a")); // 6 sondan baslayip ilk a buluyor
                                                                              // index saymaya bastan baslar
    }
}
